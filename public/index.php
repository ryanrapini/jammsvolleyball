                        <h1 class="title is-2" id="rules">
                            League Rules
                        </h1>
                        <p>Above all else, please remember that while it is ok to be competitive, the main goal of these leagues is to have&nbsp;FUN. We are a group of&nbsp;adults playing recreational volleyball. Please be considerate to others! As a captain, please ensure that your team understands this and that everyone has a fun and enjoyable session!</p><br>
                        <pre>
1. Please be considerate of others. Failure to comply may result in
removal from the league or from the premises.

2. Coed 6's teams should comprise of (at a minimum) two girls.

    a. If teams only have one girl, they can only have three guys on
    the court.

    b. If your team has no girl, you cannot play and must forfeit.

3. Games are to 21. Games must be won by two points, with a cap of 25.
In playoffs there is no cap. Since there will be no refs, teams should
keep track of the score on their own and should announce the score
before every point is served.

4. Players must rotate around the court in a fixed order. If a player
subs in for another player, they must come in and out in the same
spot.

5. No net contact is allowed. If any part of your body contacts any
part of the net (between the antennas) you have committed a fault and
the play is dead. Please be honest and call yourself!

6. In sand, you can come under the net as long as it does not
interfere with the play or present a danger to other players. Please
be cautious. If you collide with another player or prevent them from
playing the ball because you are in the way, you have committed a
fault and the play is dead.

7. Backrow players cannot block. Backrow players cannot jump to attack
the ball in front of an imaginary "10 foot" line.

8. Net serves are allowed. If the ball contacts the net on a serve and
comes over it is still playable.

9. Kicking the ball is legal. You cannot kick the ball to serve.

10. You can chase down a ball hit outside the antenna and bring it
back, as long as you bring the ball back to your side from outside the
antenna. You cannot step onto the other court to do so. Please be
careful!
</pre>
<h4 class="title is-4">Skill Level Specific Rules</h4>
<pre>
Recreational

    1. Players cannot catch and throw the ball. Contact with the ball
    should be quick. The player cannot "steer" the ball through the
    air.

    2. Players cannot lift the ball. Any contact below the waist must
    be quick and the ball cannot be "scooped".

Advanced - All the rules from Recreational, plus:

    1. If hand setting the ball, both hands must contact and release
    the ball at the same time. The set must not come below the chest
    and the ball should come out with very little rotation.

    2. Please call your own hands. If the set is bad you should know.
    Honesty is important!
